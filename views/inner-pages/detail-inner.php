				<h4 style="font-weight:bolder;">Nombre de la Ponencia</h4>

				<h5>Acceso a marcos de referencia bajo los efectos de grandes terremotos: colocación por mínimos cuadrados para la evolución post-sísmica no lineal.</h5><br>

				<p>Los modelos extendidos de trayectorias de series de tiempo GPS permiten la es-timación de parámetros de deformación co- y post-sísmica. La interpolación o ‘transferencia’ de dichos parámetros a pilares geodésicos pasivos permite pre-decir el comportamiento temporal de dichos puntos, los cuales carecen de monitoreo continuo. Esto permite la reducción de las coordenadas observadas a la época convencional del marco de referencia incluso luego de grandes sismos. En esta presentación mostraremos la importancia de los denominados ‘mod-elos de predicción de trayectorias’ y se comentará sobre los últimos adelantos para la obtención de campos de desplazamiento co-sísmicos de los sismos de 2010 en Maule, Chile y 2015 en Illapel, Chile. La metodología propuesta muestra que es posible predecir los saltos de grandes terremotos con precisiones de ~5 cm o mejores incluso en el campo cercano del terremoto.</p>
